package org.proyecto.pia_2.dto;

public class EmpleadorRegistroDTO {
    private Long id;
    private String username;
    private String password;
    private String email;
    private String confirmPassword;

    public EmpleadorRegistroDTO() {}

    public EmpleadorRegistroDTO(String email) {
        this.email = email;
    }

    public EmpleadorRegistroDTO(Long id, String username, String password, String email) {
        this.id = id;
        this.username = username;
        this.password = password;
        this.email = email;
    }

    public EmpleadorRegistroDTO(String username, String password, String email) {
        this.username = username;
        this.password = password;
        this.email = email;
    }

    public Long getId() {
        return id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getConfirmPassword() {
        return confirmPassword;
    }

    public void setConfirmPassword(String confirmPassword) {
        this.confirmPassword = confirmPassword;
    }

}
